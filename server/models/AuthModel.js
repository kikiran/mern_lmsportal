import mongoose from "mongoose";

const AuthModel = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const AuthSchema = mongoose.model("User", AuthModel);

export default AuthSchema;
